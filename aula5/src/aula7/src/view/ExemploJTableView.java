    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int indiceLinha = tblClientes.getSelectedRow();
        
        if(indiceLinha<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }else{       
            
             DefaultTableModel modeloTabela = (DefaultTableModel)tblClientes.getModel();
             modeloTabela.removeRow(indiceLinha);
        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        modoTela = "Editar";
        
        int indiceLinha = tblClientes.getSelectedRow();
        
        if(indiceLinha<0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }else{       
            
             txtNome.setText(tblClientes.getValueAt(indiceLinha, 0).toString());
             txtCPF.setText(tblClientes.getValueAt(indiceLinha, 1).toString());
             
        }
        
        txtNome.setEnabled(true);
        txtCPF.setEnabled(true);
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        //Resgato o modelo da tabela (Para adicionar novas linhas)
        DefaultTableModel modeloTabela = (DefaultTableModel)tblClientes.getModel();
        
        if(modoTela.equals("Criar")){
            modeloTabela.addRow(new String[]{txtNome.getText(),txtCPF.getText()});
        } else{
            int indiceLinha = tblClientes.getSelectedRow();
            
            //Altero os dados da linha selecionada na tabela
            tblClientes.setValueAt(txtNome.getText(), indiceLinha, 0);
            tblClientes.setValueAt(txtCPF.getText(), indiceLinha, 1);
        }
        
        txtNome.setEnabled(false);
        txtCPF.setEnabled(false);
        
        txtNome.setText("");
        txtCPF.setText("");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

