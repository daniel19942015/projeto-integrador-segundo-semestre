    public void HabilitarFormulario(){
    
        this.txtNome.setEnabled(true);
        this.txtCPF.setEnabled(true);
        this.btnSalvar.setEnabled(true);
        this.btnCancelar.setEnabled(true);
        
    }
    
    public void DesabilitarFormulario(){
    
        this.txtNome.setEnabled(false);
        this.txtCPF.setEnabled(false);
        this.btnSalvar.setEnabled(false);
        this.btnCancelar.setEnabled(false);
    }
    
    public void LimparFormulario(){
        
        this.lblIDFuncionario.setText("");
        this.txtNome.setText("");
        this.txtCPF.setText("");
    }
    
    public void CarregarTabela(){
        
        //Peço ao controller resgatar os clientes do banco de dados
        ArrayList<String[]> linhasFuncionarios = FuncionarioController.consultarFuncionarios();
        
        //Opção 1 - Crio manualmente um modelo para a tabela e defino as colunas
        DefaultTableModel tmFuncionarios = new DefaultTableModel();
        tmFuncionarios.addColumn("ID");
        tmFuncionarios.addColumn("Nome");
        tmFuncionarios.addColumn("CPF");
        jTable1.setModel(tmFuncionarios);
        
        //Removo a coluna da View (JTable) mas mantenho na model para armazenar o ID
        jTable1.removeColumn(jTable1.getColumnModel().getColumn(0));
        
        //Para cada cliente resgatado do banco de dados, atualizo a tabela
        for(String[] c:linhasFuncionarios)
        {
            tmFuncionarios.addRow(c);
        }
        
        //Defino o tamanho para cada coluna
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(300);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
